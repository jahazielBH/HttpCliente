    private void tblDepartamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDepartamentoMouseClicked
        int selecion = tblDepartamento.rowAtPoint(evt.getPoint());
        txtId.setText(tblDepartamento.getValueAt(selecion, 0) + "");
        txtNombre.setText(tblDepartamento.getValueAt(selecion, 1) + "");
        txtDescripcion.setText(tblDepartamento.getValueAt(selecion, 2) + "");
    }//GEN-LAST:event_tblDepartamentoMouseClicked

    private void btoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoBuscarActionPerformed
        try {
            json = new JSONObject(departamentoRequest.getDeptoById(txtIdB.getText()));
            modelo = ((DefaultTableModel) tblDepartamento.getModel());
            limpiarTabla();
            limpiar();
            modelo.addRow(new Object[]{
                json.get("id"),
                json.get("nombre"),
                json.get("descripcion"),
            });
            limpiar();
        } catch (Exception ex) {
            Logger.getLogger(DepartamentoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btoBuscarActionPerformed

    private void btoEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoEliminarActionPerformed
        try {
            departamentoRequest.deleteDepto(txtIdB.getText());
            limpiar();
        } catch (Exception ex) {
            Logger.getLogger(DepartamentoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btoEliminarActionPerformed

    private void btoGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoGuardarActionPerformed
        try {
            if (txtNombre.getText().isEmpty() || txtDescripcion.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Aviso! Campos del formulario vacios");
            } else {
                departamento = new Departamento();
                departamento.setNombre(txtNombre.getText());
                departamento.setDescripcion(txtDescripcion.getText());

                json = new JSONObject(departamento);
                departamentoRequest.addDepto(json.toString());
                JOptionPane.showMessageDialog(null, "Departamento Registrado");
                limpiar();
            }
        } catch (Exception ex) {
            Logger.getLogger(DepartamentoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btoGuardarActionPerformed

    private void btoActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoActualizarActionPerformed
        try {
            departamento = new Departamento();
            departamento.setId(Long.valueOf(txtId.getText()));
            departamento.setNombre(txtNombre.getText());
            departamento.setDescripcion(txtDescripcion.getText());

            json = new JSONObject(departamento);
            departamentoRequest.updateDepto(json.toString());
            JOptionPane.showMessageDialog(null, "Departamento Actualizado");
            limpiar();
        } catch (Exception ex) {
            Logger.getLogger(DepartamentoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btoActualizarActionPerformed

    private void btoListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoListarActionPerformed
        JSONArray jsonArray;
        try {
            jsonArray = new JSONArray(departamentoRequest.getAllDeptos());
            modelo = ((DefaultTableModel) tblDepartamento.getModel());
            limpiarTabla();
            Object[] ob = new Object[3];
            for (int i = 0; i < jsonArray.length(); i++) {
                ob[0] = jsonArray.getJSONObject(i).getLong("id");
                ob[1] = jsonArray.getJSONObject(i).getString("nombre");
                ob[2] = jsonArray.getJSONObject(i).getString("descripcion");
                modelo.addRow(ob);
            }
        } catch (Exception ex) {
            Logger.getLogger(DepartamentoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btoListarActionPerformed

